          <div class="col col--4x colText textContainer">
            <span class="regularText">
              <p><i>Being Collective</i> est une expérience-performance qui a mene a la realisation d'un court-nmetrage. L'objectif de ce travail collecitf était d’expérimenter des points de contacts entre des ensemble de machines, de pigeons et d’humains dans la continuité des expérimentations engagées avec <a href="iop.php"><i>L’internet des perruches</i></a> lors du premier workshop "<a href="https://reflectiveinteraction.ensadlab.fr/workshop-behavioral-matter/">Behavioral Matter 1</a>", à l’ENSCI. <BR><BR>

                <i>Being Collective</i> traduit nos essais sous la forme d’une vidéo dans laquelle on peut observer des robots tenter d’amadouer des pigeons avec des miettes de pain. Les robots tentent de s’insérer dans le groupe de pigeons qui, méﬁants, s’approchent, se reculent, s’envolent et reviennent près des robots.
              </p>
            </span>
