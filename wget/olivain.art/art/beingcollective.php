        <h1>Being collective</h1>
      </div>

      <div class="col col--4x colText textContainer">
        <span class="regularText">
          Une expérience artistique menée lors du workshop
          "<a href="https://reflectiveinteraction.ensadlab.fr/workshop-behavioral-matter-centre-pompidou/">Behavioral Matter 2</a>" au centre Pompidou de Paris en mars 2019, avec la collaboration de <a href="https://www.impossiblegeographies.net/" target="_blank">Petra Gemeinboeck</a>, <a href="https://www.jeandubois.info/" target="_blank">Jean Dubois</a>, <a href="#">Alexandre Curlet</a>, <a href="https://www.erin-gee.com/" target="_blank">Erin Gee</a> et <a href="https://www.errorishuman.com/" target="_blank">Guillaume Pascale</a>.
          <br><br>
        </span>
      </div>

