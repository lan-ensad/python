        <span class="regularText">
          G.O.R (Groupe des Objets Révolutionnaires) est un projet aux formes multiples réalisé avec Filipe Pais et Julie Brugier. Il est constitué principalement par un ensemble d’objets de récupération augmentés de moteurs et capables de mouvements. Si G.O.R peut s'incarner dans un manifeste et prendre la forme d’une installation d’éléments actifs, ces derniers ont aussi été mis en scène dans une vidéo les présentant dans la rue. Le projet, inspiré par les écrits de José Saramago (Objecto Quase, 1978) et Bruno Latour (Esquisse d’un parlement des choses, 2018), donne à voir des objets exprimant leur mécontentement sous la forme de manifestations. Groupe d’objets à comportements G.O.R propose un point de vue politique sur la situation des objets de nos vies quotidiennes et engage le spectateur à reconsidérer les éléments artiﬁciels qui l’entourent.<br><br>
