        <h1>Apophénie</h1>
      </div>

      <div class="col col--4x colText textContainer">
        <span class="regularText">
          Une tableau dynamique, une sculpture connectée, un miroir mais aussi un écran (2016).
        </span>
      </div>

      <div class="col col--4x">
        <img src="img/apophenie/apophenie2.jpg">
      </div>

      <div class="col col--4x colText textContainer">
        <span class="regularText">
          <p><i>Apophenie</i> est un dispositif qui prend la forme d'un "miroir magique", un miroir qui agit aussi comme un écran. En plus de refleter ses spectateurs, la machine fait apparaitre des visages qui défilent, se voient <i>swipés</i> d'un coté ou l'autre de l'écran.
            <br><br>
            En psychiatrie, le terme d'apophénie définit une altération de la perception dans laquelle un individu va relier entre eux des éléments qui n’ont pas de rapports. Il va alors donner du sens à des données qui n’en ont pas.
            <br><br>
            Le miroir est ici l'incarnation d'un programme informatique qui  analyse, grâce à un algorithme de reconnaissance faciale, les visages que lui transmet le service de rencontre qu'est Tinder. Ce programme évalue le degré de ressemblance entre chacun des visages proposés et celui d'une personne particulière : celui de <a href="https://fr.wikipedia.org/wiki/Victorine_Meurent" target="_blank">Victorine Meurent,</a> une peintre célèbre pour avoir été le modèle de Manet, notamment dans <i>Olympia</i> et <i>Le Déjeuner sur l’herbe</i>. C’est le visage de Victorine que cherche <i>Apophénie</i> sur Tinder en analysant tous les visages, homme et femmes, qu’il va détecter sur les profils qui lui seront proposés. De par son caractère froidement mathématique, <i>Apophénie</i> se trompe parfois, voit des visages là ou il n’y en a pas de la même manière que l’on peut reconnaitre un visage dans un nuage. Lorsqu’il trouve quelqu’un dont le visage correspond mathématiquement à celui de Victorine, il <i>like</i> le profil et passe mécaniquement au profil suivant. C'est à ce moment précis que le spectateur peut apercevoir le portrait-robot de Victorine, réalisé par ordinateur à partir de peintures qui la représentent. Cette courte réminiscence de la muse disparue passe très vite et un nouveau visage apparaît.
       	<br><br>
          Le miroir renvoie au spectateur sa propre image tout en y superposant le visage d'inconnus et de faux-positifs. Le miroir est ici une arène ou s'intègrent ensemble spectateurs de l'exposition et utilisateurs Tinder  situés à proximité de l'installation. Miroir connecté et processus de détournement, <i>Apophénie</i> se veut  un objet de réflexion sur l'expérience ontologique à l'ère de l'internet des objets et se singularise comme espace de vanité et simulacre de tradition picturale.</p>
          </p>
        </span>
