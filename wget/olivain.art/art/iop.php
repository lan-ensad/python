        <h1>Internet des perruches</h1>
      </div>

      <div class="col col--4x colText textContainer">
        <span class="regularText">
          Une expérience artistique menée lors du workshop"<a href="https://reflectiveinteraction.ensadlab.fr/workshop-behavioral-matter/">Behavioral Matter 1</a>" à l'ENSCI, en novembre 2018 avec la collaboration de Alexandre Brun, Sébastien Gens, Ragnhil Ståhl-Nielsen, Nicolas Rabault & Emilia Tikka.<br><br>
        </span>
      </div>

      <div class="col col--4x">
        <img src="img/iop/00.png">
      </div>

      <div class="col col--4x colText textContainer">
        <span class="regularText">
          <p><i>L'internet des perruches</i> est une experimentation collective visant a inventer des objets à comportements capables de servir d'intermédiaire dans le cadre de communication entre un groupe de perruches et un groupe d'invidus humains.<br><br>
          Le dispositif mis en place au cours du workshop prenait la forme d'un système linéaire de traitement d'informations : <br>trois perruches situées à différents endroits de l'Ile-de-France, étaient reliées, via un service de visio-conférence et un logiciel de classiﬁcation de sons, à trois robots à roues équipés de feutres et placés sur une surface de papier. En fonction des gazouillis émis par les perruches, les robots traçaient au sol des formes géométriques correspondantes aux états émotionnels perçus par le logiciel de classiﬁcation.
          </p>
        </span>
      </div>



