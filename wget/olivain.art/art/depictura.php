        <h1>De Pictura</h1>
      </div>

      <div class="col col--4x colText textContainer">
        <span class="regularText">
          Une imprimante autonome (2016).<br><br>
        </span>
      </div>

      <div class="col col--4x">
        <img src="img/depictura/depictura.jpg">
      </div>

      <div class="col col--4x colText textContainer">
        <span class="regularText">
          <p><i>de pictura</i> est une installation constituée d'une simple imprimante de bureau dirigée par un programme informatique. À partir d’un mot qu’il pioche aléatoirement dans un dictionnaire enregistré, le dispositif télécharge de manière aléatoire une image sur le réseau internet et se charge de transposer la structure graphique de cette image sous forme de texte. <br>
            Ce texte consiste alors en des instructions indiquant à quel endroit d’une potentielle toile placer telle couleur jusqu’à la reconstruction totale de l’image en peinture, touche par touche. <br>
            <br>
            Le programme déverse inlassablement, feuille après feuille, les instructions qu’il génère, produisant un flux indéfini de peintures potentielles laissées à l’appréciation du spectateur.
          </p>
        </span>
      </div>

