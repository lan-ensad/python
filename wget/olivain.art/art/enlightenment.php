              </video>
            </div>

            <div class="col col--4x colText textContainer">
              <span class="regularText">
                <p><i>Enlightenment</i> est une installation de cinq projecteurs de lumière aux aspects complexes, brutaux et bricolés. Disposés les uns à proximité des autres ils se tournent dans toutes les directions et illuminent l'espace d'exposition autant qu'eux-mêmes. Dans les multiples chorégraphies collectives qu'ils mettent en œuvre, les cinq modules de l'installation réagissent à l'exposition, attirent ou repoussent le public et oriente l'attention de ce dernier dans l'exposition. Leur statut dans l'espace d'exposition change ainsi régulièrement et chacun se trouve successivement placé dans la situation de statue à observer et de dispositif technique d'éclairage.
                </p>
              </span>
            </div>



