        <h1>Enlightenment</h1>
      </div>

      <div class="col col--4x colText textContainer">
        <span class="regularText">
          Une installation de projecteurs lumineux robotisés. 2020.<br><br>
        </span>
      </div>

      <div class="col col--4x">
        <video class="vidya" muted autoplay loop playsinline>
          <source src="img/enlightenment/enlight01.webm" type="video/webm">
            <source src="img/enlightenment/enlight01.mp4" type="video/mp4">
