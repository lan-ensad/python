      <div class="col col--4x colText textContainer">
        <span class="regularText">
          <p><i>Fans (gisants)</i> est une installation de plusieurs ventilateurs, tous augmentés de capteurs de mouvements. Interconnectés les uns aux autres, ils forment un réseau de type "petit monde", un réseau dont chaque élément est relié à n'importe quel autre suivant une courte chaîne de relations, inférieure à trois intermédiaires.
          </p>
        </span>
      </div>


            <div class="col col--4x">
      <iframe class="ytbFrame"  src="https://www.youtube.com/embed/YPdYTzEf7oc?controls=0" title="YouTube video player" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
    </div>

      <div class="col col--4x colText textContainer">
        <span class="regularText">
          <p>
          Couchés au sol, les ventilateurs se trouvent déplacés de leur usage traditionnel et invitent à être considérés autrement que comme des objets utilitaires au service du confort humain. Ils s'activent les uns les autres selon les déambulations des visiteurs, réagissent chacun aux capteurs des autres et habitent l'espace au rythme des bruits qu'ils produisent, du vent qu'ils diffusent et des torsions gémissantes de leurs corps.
        </p>
          </span>
        </div>

