        <h1>Tous ensemble chacun pour soi</h1>
      </div>

      <div class="col col--4x colText textContainer">
        <span class="regularText">
          Une installation de petits robots vibrants (2018),<br><br>
        </span>
      </div>

      <div class="col col--4x">
        <video class="vidya" muted autoplay loop playsinline>
          <source src="img/tecps/tecps1.webm" type="video/webm">
            <source src="img/tecps/tecps1.mp4" type="video/mp4">
              <source src="img/tecps/tecps1.mov" type="video/mp4">
              </video>
            </div>

      <div class="col col--4x colText textContainer">
        <span class="regularText">
          <p><i>Tous ensemble chacun pour soi</i> est un dispositif de petits robots vibrants qui se deploie dans des espaces d'exposition divers. Juchés sur des ressorts, ils vibrent et se déplacent relativement à la position de leurs gouvernails respectifs. En nombre variable, ils se synchronisent et se désynchronisent, enchaînent de multiples chorégraphies dans la zone qu’ils occupent et dévoilent des formes d’organisation collective diverses. Équipés chacun d’une surface réﬂéchissante qui reﬂète les lumières et éléments de l’espace d’exposition, ils habitent celui-ci, en fragmentent l’image et y expriment des états émotionnels à la fois individuels et collectifs
          </p>
        </span>
      </div>


