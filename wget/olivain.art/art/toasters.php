        <h1>Toasters</h1>
      </div>

      <div class="col col--4x colText textContainer">
        <span class="regularText">
          Une installation de grille-pain robotisés (2018).<br><br>
        </span>
      </div>

      <div class="col col--4x">
        <video class="vidya" muted autoplay loop playsinline>
          <source src="img/toasters/toasters1.webm" type="video/webm">
            <source src="img/toasters/toasters1.mp4" type="video/mp4">
              <source src="img/toasters/toasters1.mov" type="video/mp4">
              </video>
            </div>

            <div class="col col--4x colText textContainer">
              <span class="regularText">
                <p>
                  <i>Toasters</i> est un ensemble de cinq grilles-pains ordinaires, mais affranchis de leur condition utilitaire. Désormais robots, ils ne chauffent plus les tranches de pain, mais frappent le sol au rythme d'impulsions, à la manière d'un alphabet Morse ou d'un langage
                  tambouriné.
                </p>
              </span>
            </div>

            <div class="col col--4x">
              <iframe class="ytbFrame" src="https://www.youtube.com/embed/X29cfWNmojU" title="YouTube video player" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
            </div>

            <div class="col col--4x colText textContainer">
              <span class="regularText">
                <p>
                  L'installation est augmentée d'un système de quantification et qualification des
                  spectateurs permettant à ceux-ci d'expérimenter une forme d'interaction particulière : les
                  battements des grille-pains sont relatifs au nombre de spectateurs présents dans la pièce.<br>
                  En effet, lorsqu'il n'y a personne dans la salle d'exposition, les grilles-pains s'agitent. Plus
                  les spectateurs se font nombreux, moins les grilles-pains se manifestent, jusqu'à devenir
                  immobiles et silencieux lorsque la pièce est pleine.
                  Toasters cherche ainsi à expérimenter des rapports de contre-interaction. L'installation
                  instaure des relations d'intimité avec un petit nombre de spectateurs et déjoue les attentes
                  d'un public nombreux.</p>
                </span>
              </div>

