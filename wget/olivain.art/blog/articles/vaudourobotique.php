        <h1>Quimbois et robotique</h1>
      </div>


      <div class="col col--4x colText textContainer">
        <span class="regularText">
          <p>
            J'aime bien bricoler des choses et je m'engage souvent dans la fabrication de choses sans les concevoir au travers du prisme du beau et de l'efficace.
            La saleté, la fragilité, le désordonné, le disfonctionnel, sont des caractères esthétiques qui me semblent puissants. Ils sont capables d'éclipser
            la fascination que l'on peut éprouver face à ce qui correspond au "beau" et, plus généralement, à ce qui est "technique". <br>
            En m'inspirant du <a target="_blank" href="https://en.wikipedia.org/wiki/Bad_Painting">Bad painting</a> américain et <a href="https://fr.wikipedia.org/wiki/Arte_povera">l' Arte povera</a> italien, j'ai tenté et tente encore de faire des "mauvais robots".
          </p>
        </span>
      </div>

      <div class="col col--3x">
        <img src="http://olivain.art/blog/imgs/vaudou_robot_1.jpg">
      </div>

      <div class="col col--4x colText textContainer">
        <span class="regularText">
          <p>
            Ces <a target="_blank" href="https://www.reddit.com/r/shittyrobots">mauvais robots</a> prennent la forme de petites machines faites de bric et de broc qui mettent en avant une approche la moins technique possible de l'électronique.
            J'aime a penser qu'il s'agit de faire de l'électronique sans faire de l'électronique, ou plutot avec un minimum d'électronique.
            Il s'agit en effet d'utiliser le plus possible des composants comme des Lego, comme les éléments d'un kit de construction, de façon à faire un minimum de soudure et
            de consacrer un maximum de temps à l'assemblage et à la programmation d'un comportement.
          </p>
        </span>
      </div>

      <div class="col col--3x" style="background-color: white;">
        <video playsinline autoplay muted loop style="width:100%; height: 100%;">
          <source src="../imgs/voodoo_robot1.webm" type="video/webm">
          </video>
        </div>

        <div class="col col--4x colText textContainer">
          <span class="regularText">
            <p>
              L'utilisation de matériaux pauvres, tels que de la ficelle ou des chutes de bois, contraste avec les
              éléments électroniques, batteries et autres moteurs. Accessibles dans l'immédiat ils répondent à
              l'urgence d'un bricolage qui, sur l'instant, est nécessaire.
              De plus, ces machines sont douées de mouvements. Elles possèdent une forme et un comportement qui m'évoquent très vite des objets rituels.
              Dans les antilles françaises, il arrive que l'on croise de petits assemblages de matières organiques et d'objets de la vie quotidienne,
              comme des os de poulets emballés dans un sachet plastique ou des cadavres <a target="_blank" href="https://fr.wikipedia.org/wiki/Anolis">d'anolis</a> attachés à des morceaux de bois.
              Ces assemblages ensorcellés s'inscrivent dans un ensemble de pratiques rituelles que l'on nomme le <a target="_blank" href="https://fr.wikipedia.org/wiki/Quimbois">Quimbois</a>.
            </p>
          </span>
        </div>


              <div class="col col--3x">
                <img src="../imgs/quimbois_poulet_madinina.png">
                </div>

                <div class="col col--4x colText textContainer">
                  <span class="regularText">
                <p>
                  Discrètes, protégées, les techniques occultes du quimbois font la part belle à l'assemblage et au mélange. Si elles exigent de n'être pratiquées que par les initiés, voire les experts, elles
                  donnent à voir des objets bricolés avec les matériaux significatifs des alentours du bricoleur. Ces objets sont effrayants, ils témoignent d'un mauvais sort jeté, font le lien entre
                  la réalité concrète et les forces invisibles, innaccesibles au commun des mortels.
                  Et surtout, ils répondent, eux aussi, à une nécéssité absolue, un besoin impérieux, une urgence urgente.
                  <br>
                  Il y a, bien sur, de nombreux artistes qui développent un univers créatif embrassant des cosmogonies traditionnelles et qui flirtent avec le mystique.
                  Le travail de <a target="_blank" href="https://www.youtube.com/watch?v=mWY_MyZSTnE">Fernando Palma Rodriguez</a> en est un exemple criant.
                  Ses créations montrent une relation singulière aux technologies électroniques qui ne sont pas cachées. Elles font du bruit, de la chaleur, de la lumière, elles sont des mouvements, en somme elles incarnent.
                  Abordée sous l'angle du mysticisme et de l'assemblage, les composants électroniques deviennent des fétiches animés de forces (sur)naturelles avec lesquelles le bricoleur convoque
                  des symboles et des puissances. Elles ne représentent plus la victoire de l'homme sur la nature mais bien un moyen de communion avec celle-ci et transforment le bricoleur en apprenti-sorcier.

                </p>
              </span>
            </div>


