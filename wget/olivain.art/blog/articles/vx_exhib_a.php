      <div class="col col--4x colText textContainer">
        <span class="regularText">
          <p>
            A la pensée de robot éternuants les uns après les autres, le logiciel auto-réplicatif se
            révèle être un média capable de se manifester dans l'action de moteurs équipé de cette forme expressive,
            il devient utile dans un travail de création artistique au dela de sa seule imagerie et
            peut se déployer dans une metempsycose à l'intérieur d'un espace d'exposition.
            Il peut être pensé comme une création artistique, une proposition immatérielle qui s'incarne spatialement dans les comportements successifs
            des autres pièces exposées.<br>
            Il nous faut ici imaginer un scénario dans lequel un tel virus peut être acteur. Pour mener à bien notre expérience, il est nécéssaire d'identifier une cible,
            un système particulier susceptible d'être attaqué par notre programme. Le virus doit d'abord se focaliser sur des installations qui mettent en scène des mouvements.
            Les objets à comportements me semblent alors être de bonnes cibles et il se trouve que j'ai réalisé plusieurs installations qui mettent en scène des objets à comportements et dont le
            fonctionnement technique interne est chaque fois similaire. Faites de plusieurs éléments motorisés, ceux-ci s'activent en fonction d'informations transismes sur
            un réseau suivant une architecture client-serveur.
            Prenons "Toasters" et "Fans(gisants)". Dans ces deux installations, les éléments (grille-pain ou ventilateurs) sont connectés, par WiFi, à un serveur central.
            Ce dernier, un <a href="https://fr.wikipedia.org/wiki/Raspberry_Pi">Raspberry-pi</a>,
            crée un réseau WiFi et transmet des instructions à toutes les machines qui y sont connectées.
          </p>
        </span>
      </div>


      <div class="col col--3x">
        <img src="../imgs/schematics_vx_exhib_1.png">
      </div>

      <div class="col col--4x colText textContainer">
        <span class="regularText">
          <p>
            Déja, on se fait une idée précise de notre cible.
            Que ce soit "Toasters" ou "Fan(gisants)" le virus peut atteindre les moteurs qui agitent l'installation à partir du serveur.
            Pour être fonctionnel, le virus doit alors chercher à se transmettre de Raspberry-pi en Raspberry-pi.
            Comme chaque installation possède son propre réseau Wifi, il doit d'abord se connecter à celui-ci avant de trouver le serveur et l'infecter.
            Ensuite il doit intercepter les requetes envoyées par le serveur et les modifier pour perturber l'action des moteurs.
          </p>
        </span>
      </div>

      <div class="col col--3x">
        <img src="../imgs/schematics_vx_exhib_2.png">
      </div>

      <div class="col col--4x colText textContainer">
        <span class="regularText">
          <p>
            Toutes ces opérations demandent du temps. Pour se connecter au réseau WiFi, puis pour pour accéder au serveur
            il faut <a href="https://fr.wikipedia.org/wiki/Attaque_par_force_brute">bruteforcer</a> les mots de passe. En conditions réelles, de telles
            opérations peuvent prendre des mois, voire des années.
            Les contraintes techniques font de ce projet un malware inopérant et sans grand intérêt. Néanmoins, les dimensions spatiales et temporelles
            d'une maladie qui se déplace d'installations en installations fondent une réflexion quasi-curatoriale : une épidémie dans une exposition d'art numérique!
            Le programme-virus peut en effet être envisagé comme un protocole pour les machines exposées qui, elles, peuvent être conçues de façon à
            accepter l'infection. Dans cette exposition infectée, les pièces sont programmées suivant une architecture client-serveur. Elles utilisent chacune un réseau WiFi
            qui leur est propre et auquel tous les éléments d'une installation sont connectés. Un serveur, propre à chaque installation,
            transmet des informations aux éléments motorisés et délenche ainsi l'action des moteurs.
            <br><br>
            Enfin, dans cette exposition, il y a un patient zéro. Une pièce conçue pour incarner le virus et servir de base à son déploiement dans l'exposition.
            Au fur et à mesure de la durée de l'exposition le programme contenu dans le patient zéro va infecter les différentes installations et faire adopter à celles-ci des comportements malades.
            Les comportements se déplacent d'installation en installation, tout au long des scans WiFi et des attaques par bruteforce, et le programme prend forme dans une lente évolution de mouvements.
            <br><br>
          </p>
        </span>
