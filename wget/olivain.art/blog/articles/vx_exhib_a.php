        <h1>vx.exhib.a</h1>
      </div>


      <div class="col col--4x colText textContainer">
        <span class="regularText">
          <p>
            L’écriture de virus informatiques m'a toujours fasciné. Loin de moi l'idée de créer des programmes destructeurs et malveillants, bien sûr, mais lorsque l'on est attentif,
            la créativité dont font preuve les programmeurs de logiciels auto-réplicatif est affolante.
            Pour contourner les antivirus, cacher l’infection d’une machine ou, surtout, pour se manifester à l’utilisateur, ils rivalisent d’inventivité et parfois même d’humour.
            L’idée même d’un logiciel capable de se « reproduire » et de se transmettre automatiquement d’une machine à une autre
            sans intervention extérieure m’est bouleversante. <br><br>

            Ces logiciels peuvent avoir des impacts sur la  géopolitique mondiale, l’imagerie qu’ils véhiculent fait aujourd’hui partie de l’imaginaire collectif, l’idée du virus informatique est chargée
            de symboles, un <a target="_blank" href="https://archive.org/details/malwaremuseum">musée en ligne leur est même dédié</a>
            et pourtant le sujet ne semble pas avoir beaucoup été embrassé par les artistes. <br>Les <a target="_blank" href="https://www.youtube.com/watch?v=Sdga-Ttpm7Y">Computer-virus</a> de Joseph Nechvatal
            sont des images de virus, le programme <a href="https://0100101110101101.org/biennale-py/">Biennale.py</a>,
            réalisé par le coupe Mattes et le collectif [epidemic], donne à voir "l'objet virus", et <a target="_blank" href="https://www.youtube.com/watch?v=jJFlRoQr96E">loveLetter.vbs reading</a> du même collectif
            exploite davantage le langage et la symbolique du virus informatique que le virus lui-même le virus comme entité technique.
            Mais le virus à un comportement, des caractéristique expressives.
          </p>
        </span>
      </div>

      <div class="col col--3x">
        <img src="http://olivain.art/blog/imgs/kuku.com.jpg">
      </div>


      <div class="col col--4x colText textContainer">
        <span class="regularText">
          <p>
            Lorsque les gens sont malades, disons, de la grippe, on remarque qu'ils adoptent un comportement de malade. Ils ont l'air fatigués et
            éternuent dans des mouvements brusques. Quand la maladie agit, le corps réagit et des mouvements apparaissent qui révelent un état de maladie à tout observateur.
            En contaminant les individus sains à proximité du malade, le virus prend corps dans une chaine d'états comportemantaux, un réseaux de gestuelles patraques.<br>
            Les machines aussi ont de comportements et s'il existe un comportement de malade chez les humains, alors les <a target="_blank" href="https://vimeo.com/329955852">machines peuvent</a> aussi
            adopter une attitude de malade et être nauséeuses.
            Des robots qui tombent malades les uns après les autres, qui se mettent à gigoter au fur et à mesure qu'un virus informatique
            les contamine, est une image que je trouve attrayante et l'idée d'un logiciel qui manifeste son existence
            au travers de chorégraphies de moteurs, de comportements hératiques, brusques et répétitifs ou ralentis et fatigués,
            me semble ouvrir des perspectives artistiques interessantes.
          </p>
        </span>
      </div>



